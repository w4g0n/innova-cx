"""
Phase 3 — Classifier Evaluation Against LLM-Labeled Test Set
=============================================================
Reads a test CSV that has ground truth labels generated by an LLM (e.g. Claude),
runs the Phase 2 DeBERTa classifier on the texts, and compares predictions
against ground truth across all four labels.

Expected test CSV columns:
    ticket_type, text, issue_severity, issue_urgency, safety_concern, business_impact

Only complaint rows are evaluated (inquiries have no labels).

Metrics reported per label:
    - Accuracy
    - F1 score (macro)
    - Per-class precision & recall
    - Confusion matrix

Requirements:
    pip install -r data/synthesizerv7/requirements.txt

Usage:
    python phase3_evaluate.py --test test.csv

    # Save full results with predictions alongside ground truth
    python phase3_evaluate.py --test test.csv --output output/predictions.csv
"""

