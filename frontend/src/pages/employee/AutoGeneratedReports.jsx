import { useEffect, useState } from "react";
import { pdf } from "@react-pdf/renderer";
import EmployeeReportPDF from "./EmployeeReportPDF";
import Layout from "../../components/Layout";
import PillSelect from "../../components/common/PillSelect";
import PageHeader from "../../components/common/PageHeader";
import KpiCard from "../../components/common/KpiCard";
import ExportPdfButton from "../../components/common/ExportPdfButton";
import { apiUrl } from "../../config/apiBase";
import { sanitizeText, sanitizeReportId } from "./EmployeeSanitize";
import "./AutoGeneratedReports.css";

function getAuthToken() {
  try {
    const userRaw = localStorage.getItem("user");
    if (userRaw) {
      const u = JSON.parse(userRaw);
      if (u?.access_token) return u.access_token;
    }
  } catch {
    // ignore parse errors — fall through
  }
  return (
    localStorage.getItem("access_token") ||
    localStorage.getItem("token")        ||
    localStorage.getItem("jwt")          ||
    localStorage.getItem("authToken")    ||
    ""
  );
}

const API_BASE = apiUrl("/api");

// Month abbreviation → number map, used for safe URL param construction
const ABBR_TO_NUM = {
  jan:1, feb:2, mar:3, apr:4, may:5,  jun:6,
  jul:7, aug:8, sep:9, oct:10,nov:11,dec:12,
};

export default function AutoGeneratedReports() {
  const [reports,       setReports]       = useState([]);
  const [selectedId,    setSelectedId]    = useState("");
  const [selected,      setSelected]      = useState(null);
  const [loadingList,   setLoadingList]   = useState(false);
  const [loadingReport, setLoadingReport] = useState(false);
  const [refreshing,    setRefreshing]    = useState(false);
  // Fixed internal error messages — raw e.message from the network is never rendered
  const [error,         setError]         = useState("");

  // 1) Load reports list on mount
  useEffect(() => {
    const token = getAuthToken();
    if (!token) return;

    (async () => {
      setLoadingList(true);
      setError("");
      try {
        const res = await fetch(`${API_BASE}/employee/reports`, {
          headers: { Authorization: `Bearer ${token}` },
        });
        if (!res.ok) throw new Error("list_fetch_failed");
        const data = await res.json();
        const list = data.reports || [];
        setReports(list);
        if (list.length > 0) {
          // sanitizeReportId the first item's ID before storing as selectedId
          setSelectedId(sanitizeReportId(list[0].id, 20));
        }
      } catch {
        // Fixed string — never raw error.message
        setError("Failed to load reports. Please try again.");
      } finally {
        setLoadingList(false);
      }
    })();
  }, []);

  // 2) Load selected report details whenever selectedId changes
  useEffect(() => {
    const token = getAuthToken();
    if (!token || !selectedId) return;

    // selectedId is always sanitizeReportId'd before being set — safe to use in URL
    (async () => {
      setLoadingReport(true);
      setError("");
      try {
        const res = await fetch(
          `${API_BASE}/employee/reports/${encodeURIComponent(selectedId)}`,
          { headers: { Authorization: `Bearer ${token}` } }
        );
        if (!res.ok) throw new Error("report_fetch_failed");
        const data = await res.json();
        setSelected(data);
      } catch {
        setError("Failed to load report. Please try again.");
        setSelected(null);
      } finally {
        setLoadingReport(false);
      }
    })();
  }, [selectedId]);

  // 3) Regenerate the selected month's report from live ticket data
  const handleRefresh = async () => {
    const token = getAuthToken();
    if (!token || refreshing) return;

    setRefreshing(true);
    setError("");
    try {
      let year, month;

      if (selectedId && selectedId.includes("-")) {
        // selectedId is already sanitizeReportId'd — only [a-z0-9-] chars present
        const parts = selectedId.split("-");
        const abbr  = parts[0].toLowerCase();
        month = ABBR_TO_NUM[abbr];
        year  = parseInt(parts[1], 10);
        // Validate parsed values — reject if outside realistic ranges
        if (!month || !year || year < 2000 || year > 2100) {
          month = undefined;
          year  = undefined;
        }
      }

      // Fallback to last calendar month
      if (!month || !year) {
        const now = new Date();
        month = now.getMonth() === 0 ? 12 : now.getMonth();
        year  = now.getMonth() === 0 ? now.getFullYear() - 1 : now.getFullYear();
      }

      // year and month are now validated integers — safe to use as query params
      const res = await fetch(
        `${API_BASE}/employee/reports/generate?year=${year}&month=${month}`,
        { headers: { Authorization: `Bearer ${token}` } }
      );

      if (!res.ok) throw new Error("refresh_failed");

      const { reportCode } = await res.json();

      // Reload the list so the dropdown stays in sync
      const listRes  = await fetch(`${API_BASE}/employee/reports`, {
        headers: { Authorization: `Bearer ${token}` },
      });
      const listData = await listRes.json();
      const list     = listData.reports || [];
      setReports(list);

      if (reportCode) {
        // sanitizeReportId before storing — report codes come from the server
        setSelectedId(sanitizeReportId(String(reportCode), 20));
      }
    } catch {
      setError("Refresh failed. Please try again.");
    } finally {
      setRefreshing(false);
    }
  };

  // 4) PDF export — sanitize employee fields before passing to the PDF renderer
  const handleDownload = async () => {
    if (!selected) return;

    const name       = sanitizeText(selected.employeeName || "Employee", 100);
    const employeeId = sanitizeText(selected.employeeId   || "",         50);

    const blob = await pdf(
      <EmployeeReportPDF
        report={selected}
        employeeName={name}
        employeeId={employeeId}
        downloadDate={new Date()}
      />
    ).toBlob();

    const url = URL.createObjectURL(blob);
    const a   = document.createElement("a");
    a.href = url;
    // sanitizeText on name and sanitizeText on month before using in filename
    a.download = `${name}-${sanitizeText(selected.month || "", 20)}-Report.pdf`;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
  };

  const selectedMeta =
    reports.find((r) => r.id === selectedId) || reports[0] || null;

  return (
    <Layout role="employee">
      <div className="empReportsPage">
        {/* error is a fixed internal string — never raw network error text */}
        {error && <div className="empNotifs__empty">{error}</div>}

        {loadingList ? (
          <div className="empNotifs__empty">Loading reports...</div>
        ) : reports.length === 0 ? (
          <div className="empNotifs__empty">
            No reports found. Reports generate automatically once you have ticket activity.
          </div>
        ) : (
          <>
            <PageHeader
              title={`Monthly Performance Report – ${
                // Sanitize month label from API before rendering
                sanitizeText(selected?.month || selectedMeta?.month || "", 40)
              }`}
              subtitle={sanitizeText(selected?.subtitle || selectedMeta?.subtitle || "", 200)}
              actions={
                <>
                  <PillSelect
                    value={selectedId}
                    onChange={(v) => {
                      // sanitizeReportId strips chars outside [a-z0-9-] before updating state
                      const safe = sanitizeReportId(String(v), 20);
                      if (safe) setSelectedId(safe);
                    }}
                    ariaLabel="Select month report"
                    options={reports.map((r) => ({
                      value: sanitizeReportId(r.id,  20),
                      label: sanitizeText(r.month,   40),
                    }))}
                  />

                  <button
                    className="refreshReportBtn"
                    onClick={handleRefresh}
                    disabled={refreshing}
                    title="Recalculate this month's report from live ticket data"
                  >
                    <svg
                      className={refreshing ? "refreshIcon spinning" : "refreshIcon"}
                      viewBox="0 0 24 24"
                      fill="none"
                      stroke="currentColor"
                      strokeWidth="2.2"
                      strokeLinecap="round"
                      strokeLinejoin="round"
                    >
                      <polyline points="23 4 23 10 17 10" />
                      <polyline points="1 20 1 14 7 14" />
                      <path d="M3.51 9a9 9 0 0 1 14.85-3.36L23 10M1 14l4.64 4.36A9 9 0 0 0 20.49 15" />
                    </svg>
                    {refreshing ? "Refreshing…" : "Refresh Report"}
                  </button>

                  <ExportPdfButton
                    as="button"
                    className="downloadBtn"
                    onClick={handleDownload}
                    label="Export PDF"
                    disabled={!selected || loadingReport}
                  />
                </>
              }
            />

            {loadingReport || !selected ? (
              <div className="empNotifs__empty">Loading report details...</div>
            ) : (
              <>
                <section className="kpiRow">
                  <KpiCard label="Overall Rating"        value={sanitizeText(selected.kpis.rating,      30)} caption="Based on closure rate & SLA."   />
                  <KpiCard label="Complaints Resolved"   value={selected.kpis.resolved}                      caption="All priorities combined."        />
                  <KpiCard label="SLA Compliance"        value={sanitizeText(selected.kpis.sla,         20)} caption="Resolved within agreed time."    />
                  <KpiCard label="Avg Response Time"     value={sanitizeText(selected.kpis.avgResponse, 40)} caption="Time to first update."           />
                </section>

                <section className="reportGrid">
                  <article className="repCard">
                    <h2 className="repCardTitle">Summary Breakdown</h2>
                    <p className="repCardSubtitle">
                      Snapshot of how you handled tickets this month.
                    </p>

                    <div className="summaryGrid">
                      {selected.summary.map((item) => (
                        <div key={item.label} className="summaryItem">
                          {/* Sanitize server-supplied label and value before rendering */}
                          <span className="summaryLabel">{sanitizeText(item.label, 80)}</span>
                          <span className="summaryValue">{sanitizeText(String(item.value ?? ""), 80)}</span>
                        </div>
                      ))}
                    </div>
                  </article>

                  <aside className="repCard">
                    <h2 className="repCardTitle">Rating Components</h2>
                    <p className="repCardSubtitle">
                      How your overall score was calculated.
                    </p>

                    <div className="ratingList">
                      {selected.ratingComponents.map((rc) => (
                        <div key={rc.name} className="ratingRow">
                          {/* Sanitize server-supplied rating component fields */}
                          <span className="ratingName">{sanitizeText(rc.name, 80)}</span>
                          <div className="ratingBar">
                            <div
                              className="ratingBarFill"
                              // rc.pct is a number — coerce safely, cap at 100
                              style={{ width: `${Math.min(100, Math.max(0, Number(rc.pct) || 0))}%` }}
                            />
                          </div>
                          <span className="ratingScore">
                            {Number(rc.score).toFixed(1)}
                          </span>
                        </div>
                      ))}
                    </div>
                  </aside>
                </section>

                <section className="tableWrapper">
                  <h2 className="repCardTitle">Weekly Activity Overview</h2>
                  <p className="repCardSubtitle">
                    Distribution of tickets across the month.
                  </p>

                  <div className="weekTableWrap">
                    <table className="weekTable">
                      <thead>
                        <tr>
                          <th>Week</th>
                          <th>Tickets Assigned</th>
                          <th>Resolved</th>
                          <th>SLA Compliance</th>
                          <th>Avg Response Time</th>
                          <th>Change vs Previous Week</th>
                        </tr>
                      </thead>
                      <tbody>
                        {selected.weekly.map((row) => (
                          <tr key={row.week}>
                            {/* Sanitize all server-supplied weekly row fields before rendering */}
                            <td>{sanitizeText(String(row.week     ?? ""), 40)}</td>
                            <td>{sanitizeText(String(row.assigned ?? ""), 40)}</td>
                            <td>{sanitizeText(String(row.resolved ?? ""), 40)}</td>
                            <td>{sanitizeText(String(row.sla      ?? ""), 40)}</td>
                            <td>{sanitizeText(String(row.avg      ?? ""), 40)}</td>
                            <td>
                              <span
                                className={
                                  row.delta?.type === "positive"
                                    ? "deltaPositive"
                                    : "deltaNeutral"
                                }
                              >
                                {sanitizeText(String(row.delta?.text ?? ""), 40)}
                              </span>
                            </td>
                          </tr>
                        ))}
                      </tbody>
                    </table>
                  </div>

                  <ul className="notesList">
                    {selected.notes.map((n, i) => (
                      // Sanitize server-supplied notes before rendering
                      <li key={i}>{sanitizeText(String(n ?? ""), 500)}</li>
                    ))}
                  </ul>
                </section>
              </>
            )}
          </>
        )}
      </div>
    </Layout>
  );
}