import { BrowserRouter, Routes, Route } from "react-router-dom";
import { Suspense, lazy } from "react";

import ProtectedRoute from "./auth/ProtectedRoute";

// Lazily loaded pages (prevents whole app from blanking if one page has an error)
const Login = lazy(() => import("./pages/Login"));
const ForgotPassword = lazy(() => import("./pages/ForgotPassword"));

// Customer
const CustomerLanding = lazy(() => import("./pages/customer/CustomerLanding"));
const CustomerChatbot = lazy(() => import("./pages/customer/CustomerChatbot"));
const CustomerNotifications = lazy(() =>
  import("./pages/customer/CustomerNotifications")
);
const CustomerHistory = lazy(() => import("./pages/customer/CustomerHistory"));
const CustomerFillForm = lazy(() => import("./pages/customer/CustomerFillForm"));
const CustomerTicketDetails = lazy(() =>
  import("./pages/customer/CustomerTicketDetails")
);

// Employee
const EmployeeNotifications = lazy(() =>
  import("./pages/employee/EmployeeNotifications")
);
const AutoGeneratedReports = lazy(() =>
  import("./pages/employee/AutoGeneratedReports")
);
const ComplaintDetails = lazy(() => import("./pages/employee/ComplaintDetails"));
const EmployeeDashboard = lazy(() => import("./pages/employee/EmployeeDashboard"));
const EmployeeViewAllComplaints = lazy(() =>
  import("./pages/employee/EmployeeViewAllComplaints")
);

// Manager
const ManagerNotifications = lazy(() =>
  import("./pages/manager/ManagerNotifications")
);
const Approvals = lazy(() => import("./pages/manager/Approvals"));
const ComplaintTrends = lazy(() => import("./pages/manager/ComplaintTrends"));
const ManagerDashboard = lazy(() => import("./pages/manager/ManagerDashboard"));
const ManagerViewAllComplaints = lazy(() =>
  import("./pages/manager/ManagerViewAllComplaints")
);
const ViewEmployees = lazy(() => import("./pages/manager/ManagerViewEmployees"));

// Operator
const OperatorNotifications = lazy(() =>
  import("./pages/operator/OperatorNotifications")
);
const ChatbotAnalysis = lazy(() => import("./pages/operator/ChatbotAnalysis"));
const ModelAnalysis = lazy(() => import("./pages/operator/ModelAnalysis"));
const OperatorDashboard = lazy(() => import("./pages/operator/OperatorDashboard"));

export default function App() {
  return (
    <BrowserRouter>
      <Suspense
        fallback={
          <div style={{ padding: 20, fontFamily: "system-ui" }}>
            Loading…
          </div>
        }
      >
        <Routes>
          {/* Public */}
          <Route path="/" element={<Login />} />
          <Route path="/forgot-password" element={<ForgotPassword />} />

          {/* Customer public landing (if you want it public) */}
          <Route path="/customer" element={<CustomerLanding />} />

          {/* Customer (protected) */}
          <Route
            path="/customer/dashboard"
            element={
              <ProtectedRoute role="customer">
                <CustomerLanding />
              </ProtectedRoute>
            }
          />
          <Route
            path="/customer/chatbot"
            element={
              <ProtectedRoute role="customer">
                <CustomerChatbot />
              </ProtectedRoute>
            }
          />
          <Route
            path="/customer/notifications"
            element={
              <ProtectedRoute role="customer">
                <CustomerNotifications />
              </ProtectedRoute>
            }
          />
          <Route
            path="/customer/history"
            element={
              <ProtectedRoute role="customer">
                <CustomerHistory />
              </ProtectedRoute>
            }
          />
          <Route
            path="/customer/fill-form"
            element={
              <ProtectedRoute role="customer">
                <CustomerFillForm />
              </ProtectedRoute>
            }
          />
          <Route
            path="/customer/ticket/:id"
            element={
              <ProtectedRoute role="customer">
                <CustomerTicketDetails />
              </ProtectedRoute>
            }
          />

          {/* Employee (protected) */}
          <Route
            path="/employee"
            element={
              <ProtectedRoute role="employee">
                <EmployeeDashboard />
              </ProtectedRoute>
            }
          />
          <Route
            path="/employee/complaints"
            element={
              <ProtectedRoute role="employee">
                <EmployeeViewAllComplaints />
              </ProtectedRoute>
            }
          />
          <Route
            path="/employee/details/:id"
            element={
              <ProtectedRoute role="employee">
                <ComplaintDetails />
              </ProtectedRoute>
            }
          />
          <Route
            path="/employee/reports"
            element={
              <ProtectedRoute role="employee">
                <AutoGeneratedReports />
              </ProtectedRoute>
            }
          />
          <Route
            path="/employee/notifications"
            element={
              <ProtectedRoute role="employee">
                <EmployeeNotifications />
              </ProtectedRoute>
            }
          />

          {/* Manager (protected) */}
          <Route
            path="/manager"
            element={
              <ProtectedRoute role="manager">
                <ManagerDashboard />
              </ProtectedRoute>
            }
          />
          <Route
            path="/manager/complaints"
            element={
              <ProtectedRoute role="manager">
                <ManagerViewAllComplaints />
              </ProtectedRoute>
            }
          />
          <Route
            path="/manager/employees"
            element={
              <ProtectedRoute role="manager">
                <ViewEmployees />
              </ProtectedRoute>
            }
          />
          <Route
            path="/manager/approvals"
            element={
              <ProtectedRoute role="manager">
                <Approvals />
              </ProtectedRoute>
            }
          />
          <Route
            path="/manager/trends"
            element={
              <ProtectedRoute role="manager">
                <ComplaintTrends />
              </ProtectedRoute>
            }
          />
          <Route
            path="/manager/notifications"
            element={
              <ProtectedRoute role="manager">
                <ManagerNotifications />
              </ProtectedRoute>
            }
          />

          {/* Operator (protected) */}
          <Route
            path="/operator"
            element={
              <ProtectedRoute role="operator">
                <OperatorDashboard />
              </ProtectedRoute>
            }
          />
          <Route
            path="/operator/model-analysis"
            element={
              <ProtectedRoute role="operator">
                <ModelAnalysis />
              </ProtectedRoute>
            }
          />
          <Route
            path="/operator/chatbot-analysis"
            element={
              <ProtectedRoute role="operator">
                <ChatbotAnalysis />
              </ProtectedRoute>
            }
          />
          <Route
            path="/operator/notifications"
            element={
              <ProtectedRoute role="operator">
                <OperatorNotifications />
              </ProtectedRoute>
            }
          />
        </Routes>
      </Suspense>
    </BrowserRouter>
  );
}
